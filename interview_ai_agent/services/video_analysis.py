from __future__ import annotations

import json
import os
import re
import base64
import io
from typing import Any

import cv2
import requests
from PIL import Image


DEFAULT_EMOTION_LLAMA_API_URL = os.getenv("EMOTION_LLAMA_API_URL", "http://127.0.0.1:7889/api/predict/")
FALLBACK_EMOTION_LLAMA_API_URL = "http://127.0.0.1:7860/api/predict/"


def _extract_json(text: str) -> dict[str, Any] | None:
    try:
        return json.loads(text)
    except json.JSONDecodeError:
        pass

    match = re.search(r"\{[\s\S]*\}", text)
    if not match:
        return None


def _first_frame_base64(video_path: str) -> str:
    cap = cv2.VideoCapture(video_path)
    if not cap.isOpened():
        raise ValueError(f"Cannot open video: {video_path}")

    ok, frame = cap.read()
    cap.release()
    if not ok or frame is None:
        raise ValueError(f"Could not read first frame from video: {video_path}")

    frame_rgb = cv2.cvtColor(frame, cv2.COLOR_BGR2RGB)
    pil_img = Image.fromarray(frame_rgb)
    buffer = io.BytesIO()
    pil_img.save(buffer, format="PNG")
    return base64.b64encode(buffer.getvalue()).decode("utf-8")

